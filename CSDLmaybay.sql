DROP TABLE Ve
DROP TABLE KhachHang
DROP TABLE NhanVien
DROP TABLE ChiNhanh
DROP TABLE ChuyenBay
DROP TABLE MayBay
DROP TABLE SanBay

CREATE TABLE SanBay (
  MaSB VARCHAR(10) PRIMARY KEY,
  TenSB NVARCHAR(255) NOT NULL,
  DiaChi NVARCHAR(255) NOT NULL
);

CREATE TABLE MayBay (
  MaMB VARCHAR(10) PRIMARY KEY,
  TenMB NVARCHAR(255) NOT NULL,
  SoGhe INT NOT NULL,
  Hang NVARCHAR(20)
);

CREATE TABLE ChuyenBay (
  MaCB VARCHAR(10) PRIMARY KEY,
  MaMB VARCHAR(10) NOT NULL,
  SanBayXuatPhat VARCHAR(10) NOT NULL,
  SanBayDich VARCHAR(10) NOT NULL,
  ThoiGianBatDau DATETIME,
  ThoiGianKetThuc DATETIME,
  SLChoNgoiConLai INT,
  FOREIGN KEY (MaMB) REFERENCES MayBay(MaMB),
  FOREIGN KEY (SanBayXuatPhat) REFERENCES SanBay(MASB),
  FOREIGN KEY (SanBayDich) REFERENCES SanBay(MASB)
);

CREATE TABLE ChiNhanh (
  MaCN VARCHAR(10) PRIMARY KEY,
  TenCN NVARCHAR(255) NOT NULL,
  DiaChi NVARCHAR(255) NOT NULL
);

CREATE TABLE NhanVien (
  MaNV VARCHAR(10) PRIMARY KEY,
  TenNV NVARCHAR(255) NOT NULL,
  MaCN VARCHAR(10) NOT NULL,
  NgaySinh DATE NOT NULL,
  Sdt VARCHAR(255) NOT NULL,
  ChucVu NVARCHAR(255) NOT NULL,
  FOREIGN KEY (MaCN) REFERENCES CHINHANH(MaCN)
);

CREATE TABLE KhachHang (
  MaKH VARCHAR(10) PRIMARY KEY,
  TenKH NVARCHAR(255) NOT NULL,
  NgaySinh DATE NOT NULL,
  DiaChi NVARCHAR(255) NOT NULL,
  Sdt VARCHAR(255) NOT NULL,
  SoHoChieu VARCHAR(255) NOT NULL
);

CREATE TABLE Ve (
  MaV VARCHAR(10) PRIMARY KEY,
  MaKH VARCHAR(10) NOT NULL,
  MaCB VARCHAR(10) NOT NULL,
  MaNV VARCHAR(10) NOT NULL,
  GiaVe FLOAT NOT NULL,
  FOREIGN KEY (MaKH) REFERENCES KhachHang(MaKH),
  FOREIGN KEY (MaCB) REFERENCES ChuyenBay(MaCB),
  FOREIGN KEY (MaNV) REFERENCES NhanVien(MaNV)
);


